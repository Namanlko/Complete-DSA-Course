// 9) Scope of Variables.

/*
In general, the scope is defined as the extent up to which something can be worked with. In programming also the scope of variable is defined as the extent of the program code within which the variable can be accessed or declared or worked with.

There are mainly two types of scopes in varaibles.
1) Local Variables
2) Global Variables

Local Variables: Variables defined within a function or block are said to be local to those functions.

Global Variables: Global Variables can be accessed from any part of the program.
*/