// 2) Increment & Decrement Operator in C++:


#include <iostream>
using namespace std;

int main() {

  // Post Increment.
  int p = 5;
  cout<<"p++ is "<<p++<<endl;
  cout<<"p is "<<p<<endl;

  // Post Decrement.
  int q = 5;
  cout<<"q-- is "<<q--<<endl;
  cout<<"q is "<<q<<endl;

  // Pre Increment.
  int r = 5;
  cout<<"++r is "<<++r<<endl;
  cout<<"r is "<<r<<endl;

  // Pre Decrement.
  int t = 5;
  cout<<"--t is "<<--t<<endl;
  cout<<"t is "<<t<<endl;
  
  return 0;
}